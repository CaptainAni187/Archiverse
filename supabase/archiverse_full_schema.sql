create or replace function public.decrement_artwork_stock(_artwork_ids bigint[])
returns void
language plpgsql
as $$
declare
  aid bigint;
  current_qty integer;
begin
  for aid in select unnest(_artwork_ids)
  loop
    select quantity
    into current_qty
    from public.artworks
    where id = aid
    for update;

    if current_qty is null then
      raise exception 'artwork % not found', aid;
    end if;

    if current_qty <= 0 then
      raise exception 'artwork % is out of stock', aid;
    end if;
  end loop;

  update public.artworks
  set quantity = quantity - 1
  where id = any(_artwork_ids);
end;
$$;

create or replace function public.validate_combo_artworks(_artwork_ids bigint[])
returns void
language plpgsql
as $$
declare
  total_count integer;
  distinct_count integer;
begin
  total_count := coalesce(array_length(_artwork_ids, 1), 0);

  if total_count < 2 or total_count > 5 then
    raise exception 'combo must have between 2 and 5 artworks';
  end if;

  select count(distinct x)
  into distinct_count
  from unnest(_artwork_ids) as x;

  if total_count != distinct_count then
    raise exception 'duplicate artworks in combo not allowed';
  end if;
end;
$$;

