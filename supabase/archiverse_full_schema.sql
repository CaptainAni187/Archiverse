do $$
begin
  if exists (
    select 1
    from information_schema.columns
    where table_schema = 'public'
      and table_name = 'admin_activity_logs'
      and column_name = 'action'
  ) then
    execute $sql$
      update public.admin_activity_logs
      set action_type = coalesce(action_type, action)
    $sql$;
  end if;

  if exists (
    select 1
    from information_schema.columns
    where table_schema = 'public'
      and table_name = 'admin_activity_logs'
      and column_name = 'entity'
  ) then
    execute $sql$
      update public.admin_activity_logs
      set resource_type = coalesce(nullif(resource_type, ''), entity, 'system')
    $sql$;
  end if;

  if exists (
    select 1
    from information_schema.columns
    where table_schema = 'public'
      and table_name = 'admin_activity_logs'
      and column_name = 'entity_id'
  ) then
    execute $sql$
      update public.admin_activity_logs
      set resource_id = coalesce(resource_id, entity_id::text)
    $sql$;
  end if;

  if exists (
    select 1
    from information_schema.columns
    where table_schema = 'public'
      and table_name = 'admin_activity_logs'
      and column_name = 'metadata'
  ) then
    execute $sql$
      update public.admin_activity_logs
      set details = coalesce(details, metadata, '{}'::jsonb)
    $sql$;
  end if;
end $$;

update public.admin_activity_logs
